class Emoji extends PaintFunction {
    constructor(contextReal, contextDraft, colorStroke, colorFill, strokeWidth, strokeDash) {
        super();
        this.contextReal = contextReal;
        this.contextDraft = contextDraft; 
        this.colorStroke = colorStroke;
        this.colorFill = colorFill;
        this.strokeWidth = strokeWidth;
        this.strokeDash = strokeDash;
    }
    changeStrokeColor(newStrokeColor) {
    }
    changeFillColor(newFillColor) {
    }
    changeStrokeWidth(newStrokeWidth) {
    }
    changeStrokeDash(newStrokeDash) {
    }

    onMouseDown(coord, event) {
        var imgSrc;
        twemoji.parse($('select[name="emoji"]').val(), function(icon, options, variant, imgg) {
            imgSrc = "https://twemoji.maxcdn.com/"+ options.size + '/' + icon + '.png';
        });
        this.newImg = new Image();
        this.newImg.crossOrigin = "Anonymous";

        this.newImg.onload = () => {
            this.origPt = coord;
            this.contextReal.drawImage(this.newImg, coord[0]-40, coord[1]-40);
        }
        this.newImg.src = imgSrc;

        // make sure the load event fires for cached images too
        if ( this.newImg.complete || this.newImg.complete === undefined ) {
            this.newImg.src = "data:image/gif;base64,R0lGODlhAQABAIAAAAAAAP///ywAAAAAAQABAAACAUwAOw==";
            this.newImg.src = imgSrc;
        }
    }

    onMouseUp() {
        //Add the following code when you draw on canvas real for undo
        saveCanvas();
    }
}

